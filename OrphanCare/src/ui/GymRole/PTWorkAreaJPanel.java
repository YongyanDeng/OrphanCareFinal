    private void populateRequestTable() {

        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);

        for (Organization o : fitness.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof PTOrganization) {
                for (UserAccount ua : o.getUserAccountDirectory().getUserAccountList()) {
                    for (WorkRequest wr : ua.getWorkQueue().getWorkRequestList()) {

                        SendToFitnessRequest sf = (SendToFitnessRequest) wr;
                        Object[] row = new Object[5];

                        row[0] = sf;
                        row[1] = sf.getChild().getName();
                        row[2] = sf.getChild().getGender();
                        row[3] = sf.getChild().getFitnessStatus();
                        row[4] = sf.getChild().getFitnessItem();
                        model.addRow(row);

                    }
                }
            }
        }

    }

