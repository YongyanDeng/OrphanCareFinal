    private void btnAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignActionPerformed

        Integer selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table");
            return;
        }

        String fitnessItem = (String) cmbPT.getSelectedItem();
        if (fitnessItem == null) {
            JOptionPane.showMessageDialog(null, "Please select a PT Project!");
            return;
        }

        SendToFitnessRequest sf = (SendToFitnessRequest) workRequestJTable.getValueAt(selectedRow, 0);

        if (sf.getChild().getFitnessStatus().equals("Enrolled")) {

            sf.getChild().setFitnessItem(fitnessItem);

        } else {

            JOptionPane.showMessageDialog(null, "This Child Is Not Enrolled Yet!");
            return;

        }

        populateRequestTable();

    }//GEN-LAST:event_btnAssignActionPerformed

