    private void btnReceiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceiveActionPerformed

        Integer selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row from table");
            return;
        }

        SendToFitnessRequest sf = (SendToFitnessRequest) workRequestJTable.getValueAt(selectedRow, 0);

        if (sf.getChild().getFitnessStatus().equals("Enrolled")) {

            JOptionPane.showMessageDialog(null, "This Child is Already Enrolled!");
            return;

        } else {

            sf.getChild().setFitnessStatus("Enrolled");

        }

        populateRequestTable();

    }//GEN-LAST:event_btnReceiveActionPerformed

