    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) receiveJTable.getModel();
        model.setRowCount(0);

        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = ((SendToSchoolRequest) request).getStatus();
            row[2] = ((SendToSchoolRequest) request).getDistribution() == null ? "" : ((SendToSchoolRequest) request).getDistribution().getStatus();
            row[3] = ((SendToSchoolRequest) request).getComments();
            model.addRow(row);
        };
    }

