    private void btndistributionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndistributionActionPerformed
        // TODO add your handling code here:
        Integer selectedRow = receiveJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row, no selected is not permitted");
            return;
        }
        SendToSchoolRequest chcrequest = (SendToSchoolRequest) receiveJTable.getValueAt(selectedRow, 0);

        if (chcrequest.getStatus().equals("Finished")) {
            JOptionPane.showMessageDialog(null, "This request has been finished now");
            return;
        }
        if (chcrequest.getStatus().equals("Refuse")) {
            JOptionPane.showMessageDialog(null, "This request has been Refused");
            return;
        }
        //chcrequest.childName();
        chcrequest.setStatus("Processing now");
        DistributionWorkAreaJPanel dwaJPanel = new DistributionWorkAreaJPanel(userProcessContainer, userAccount, enterprise, chcrequest, business);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.add("DistributionWorkAreaJPanel", dwaJPanel);
        layout.next(userProcessContainer);
        dB4OUtil.storeSystem(business);

    }//GEN-LAST:event_btndistributionActionPerformed

